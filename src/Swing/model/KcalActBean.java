package Swing.model;

public class KcalActBean {
	String actname, actunit, actetc;
	double actkcal;

	public String getActname() {
		return actname;
	}

	public void setActname(String actname) {
		this.actname = actname;
	}

	public String getActunit() {
		return actunit;
	}

	public void setActunit(String actunit) {
		this.actunit = actunit;
	}

	public String getActetc() {
		return actetc;
	}

	public void setActetc(String actetc) {
		this.actetc = actetc;
	}

	public double getActkcal() {
		return actkcal;
	}

	public void setActkcal(double actkcal) {
		this.actkcal = actkcal;
	}
}
