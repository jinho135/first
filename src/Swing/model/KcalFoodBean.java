package Swing.model;

public class KcalFoodBean {
	String foodname, foodunit, foodetc;
	double foodkcal;

	public String getFoodname() {
		return foodname;
	}

	public void setFoodname(String foodname) {
		this.foodname = foodname;
	}

	public String getFoodunit() {
		return foodunit;
	}

	public void setFoodunit(String foodunit) {
		this.foodunit = foodunit;
	}

	public String getFoodetc() {
		return foodetc;
	}

	public void setFoodetc(String foodetc) {
		this.foodetc = foodetc;
	}

	public double getFoodkcal() {
		return foodkcal;
	}

	public void setFoodkcal(double foodkcal) {
		this.foodkcal = foodkcal;
	}
	
}
